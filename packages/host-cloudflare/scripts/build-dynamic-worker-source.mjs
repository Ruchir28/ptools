/**
 * @file Build script for the fixed Cloudflare platform-kernel source string.
 *
 * Host-cloudflare owns the Dynamic Worker artifact it asks Cloudflare to run.
 * This script bundles `cloudflareKernelAdapter.ts` together with the shared
 * `@ptools/executor/sandbox` kernel, leaves `./generated-code.js` external so
 * it can be supplied per execution, and writes the resulting JavaScript source
 * into `cloudflareKernelAdapterSource.ts` for runtime Worker Loader calls.
 */
import { mkdir, writeFile } from "node:fs/promises";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";
import { build } from "esbuild";

const packageRoot = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const entrypoint = resolve(
  packageRoot,
  "src/executor/dynamic-worker/cloudflareKernelAdapter.ts",
);
const outfile = resolve(
  packageRoot,
  "src/executor/dynamic-worker/cloudflareKernelAdapterSource.ts",
);

const result = await build({
  entryPoints: [entrypoint],
  bundle: true,
  format: "esm",
  platform: "neutral",
  target: "es2022",
  external: ["cloudflare:workers", "./generated-code.js"],
  write: false,
});

const source = result.outputFiles[0].text;
const generated = `/**\n * Built JavaScript source for the fixed Cloudflare platform-kernel adapter.\n *\n * Generated by scripts/build-dynamic-worker-source.mjs from\n * src/executor/dynamic-worker/cloudflareKernelAdapter.ts. Do not edit by hand.\n */\nexport const DYNAMIC_SANDBOX_WORKER_SOURCE = ${JSON.stringify(source)};\n`;

await mkdir(dirname(outfile), { recursive: true });
await writeFile(outfile, generated);
